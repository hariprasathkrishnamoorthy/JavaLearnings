package DataOperators;

public class BitwiseANDExample {
    public static void main(String[] args) {
        int num1 = 5;  // Binary: 0101
        int num2 = 3;  // Binary: 0011

        int result = num1 & num2;  // Bitwise AND

        System.out.println("Result of num1 & num2: " + result);  // Output: 1 (Binary: 0001)
    }
}

