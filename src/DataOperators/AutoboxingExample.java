package DataOperators;

public class AutoboxingExample {
    public static void main(String[] args) {
        // Autoboxing: Converting int to Integer
        Integer num1 = 42; // Autoboxing

        // Autoboxing also works with other primitive types
        Double num2 = 3.14; // Autoboxing

        // Autoboxing with Boolean
        Boolean flag = true; // Autoboxing

        System.out.println("Autoboxing Examples:");
        System.out.println("num1: " + num1);
        System.out.println("num2: " + num2);
        System.out.println("flag: " + flag);
    }
}

