package DataOperators;

public class BitwiseXORExample {
    public static void main(String[] args) {
        int num1 = 5;  // Binary: 0101
        int num2 = 3;  // Binary: 0011

        int result = num1 ^ num2;  // Bitwise XOR

        System.out.println("Result of num1 ^ num2: " + result);  // Output: 6 (Binary: 0110)
    }
}

