package DataOperators;

public class BitwiseORExample {
    public static void main(String[] args) {
        int num1 = 5;  // Binary: 0101
        int num2 = 3;  // Binary: 0011

        int result = num1 | num2;  // Bitwise OR

        System.out.println("Result of num1 | num2: " + result);  // Output: 7 (Binary: 0111)
    }
}

